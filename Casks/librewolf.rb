  system_command "/usr/bin/xattr",
                 args: ["-d", "com.apple.quarantine", "#{appdir}/LibreWolf.app"],
                 sudo: false
end


  zap trash: [
    "~/.librewolf",
    "~/Library/Application Support/LibreWolf",
